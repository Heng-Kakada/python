# String method : len() find() capitalize() upper() lower() isdigit() isalpha() count() replace()

name = "brian"
number = "1233"

print(len(name))
print(name.find("b"))
print(name.capitalize())
print(name.lower())
print(name.upper())
# check digit as sting
print(number.isdigit())
print(name.isalpha())
# count letter in string
print(name.count("n"))
print(name.replace("n", "t"))
